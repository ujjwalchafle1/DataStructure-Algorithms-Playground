//: What is Hash Table?
//:- Hash tables are used to store key-value pairs.
//:- They are like arrays, but the keys are not ordered.
//:- Unlike arrays hash tables are fast at all of the following operations: finding values, adding new values and removing values.
//:**********
//: What is Hashing algorithm?
//:- To implement hash table we will be using an array.
//:- In order to loop up values by key, we need a way to convert keys into valid array indicies.
//:- A function that performs this task is called hash function.
//:**********
//: Waht makes a good hashing algorithm?
//:1. Fast (i.e constant time)
//:2. Dosen't cluster output at specific indices, but distributes uniformaly.
//:3. Deterministic (same input gives same output)
